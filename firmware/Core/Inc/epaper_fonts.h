/* Character width info */
#define FONT8_WIDTH     6
#define FONT8_HEIGHT    8
#define FONT16_WIDTH    8
#define FONT16_HEIGHT   16

/* Get font data based on size */
static inline const uint8_t* EPD_GetFontData(uint8_t font_size) {
    switch (font_size) {
        case 16:
            return Font16x8;
        case 8:
        default:
            return Font8x6;
    }
}

/* Get font width */
static inline uint8_t EPD_GetFontWidth(uint8_t font_size) {
    switch (font_size) {
        case 16:
            return FONT16_WIDTH;
        case 8:
        default:
            return FONT8_WIDTH;
    }
}

/* Get font height */
static inline uint8_t EPD_GetFontHeight(uint8_t font_size) {
    switch (font_size) {
        case 16:
            return FONT16_HEIGHT;
        case 8:
        default:
            return FONT8_HEIGHT;
    }
}

